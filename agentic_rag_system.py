from rouge_score import rouge_scorer
import pandas as pd

scorer = rouge_scorer.RougeScorer(['rouge1'], use_stemmer=True)

results = []

for item in test_data:
    output = ask(item["query"])

    predicted_type = classify_query(item["query"])
    expected_type = item["type"]

    predicted_answer = generate_answer(item["query"], predicted_type)
    expected_answer = item["expected"]

    # ROUGE score
    rouge = scorer.score(expected_answer, predicted_answer)['rouge1'].fmeasure

    results.append({
        "query": item["query"],
        "expected_type": expected_type,
        "predicted_type": predicted_type,
        "rouge_score": round(rouge, 3)
    })

df = pd.DataFrame(results)

from sentence_transformers import SentenceTransformer
import faiss
import numpy as np

model = SentenceTransformer('all-MiniLM-L6-v2')

embeddings = model.encode(all_chunks)

dimension = embeddings.shape[1]
index = faiss.IndexFlatL2(dimension)
index.add(np.array(embeddings))

print("FAISS index ready")

def classify_query(query):
    query = query.lower()

    if any(word in query for word in ["compare", "difference", "impact", "analyze"]):
        return "synthesis"

    elif any(word in query for word in ["what", "who", "when", "where", "define"]):
        return "factual"

    else:
        return "out_of_scope"

def retrieve(query, k=3):
    query_embedding = model.encode([query])
    distances, indices = index.search(np.array(query_embedding), k)

    return [all_chunks[i] for i in indices[0]]

def generate_answer(query, query_type):

    if query_type == "out_of_scope":
        return "The information is not available in the provided documents."

    chunks = retrieve(query)
    return " ".join(chunks)

def ask(query):
    q_type = classify_query(query)
    answer = generate_answer(query, q_type)

    print("Query Type:", q_type)
    print("Answer:", answer[:300])

